<script>
	const prev = document.querySelector(".prev");
const next = document.querySelector(".next");
const carousel = document.querySelector(".carousel-container");
const track = document.querySelector(".track");
const slides = document.querySelectorAll(".card-container");

let slideWidth = slides[0].offsetWidth;
let index = 0;
let timer; // Timer variable for automatic sliding

window.addEventListener("resize", function () {
  slideWidth = slides[0].offsetWidth;
});

next.addEventListener("click", function (e) {
  e.preventDefault();
  moveToNextSlide();
});

prev.addEventListener("click", function (e) {
  e.preventDefault();
  moveToPrevSlide();
});

// Function to move to the next slide
function moveToNextSlide() {
  index = (index + 1) % slides.length; // Circular index increment
  moveCarousel();
}

// Function to move to the previous slide
function moveToPrevSlide() {
  index = (index - 1 + slides.length) % slides.length; // Circular index decrement
  moveCarousel();
}

// Function to move the carousel
function moveCarousel() {
  track.style.transform = `translateX(${-index * slideWidth}px)`;

  // Show or hide prev/next buttons based on index
  prev.classList.toggle("show", index !== 0);
  next.classList.toggle("show", index !== slides.length - 1);
}

// Function to automatically advance the carousel
function startCarousel() {
  timer = setInterval(moveToNextSlide, 3000); // Adjust interval as needed (3000ms = 3 seconds)
}

// Function to stop automatic carousel
function stopCarousel() {
  clearInterval(timer);
}

// Start automatic carousel when page loads
startCarousel();

// Pause automatic carousel on hover
carousel.addEventListener("mouseenter", stopCarousel);

// Resume automatic carousel on mouse leave
carousel.addEventListener("mouseleave", startCarousel);
</script>
